function DB_getColFlag(val,valflag){
	val = val.replace(/-/,'');
	if(valflag == '1'||valflag == '2'){
		return "<div class='wfl f"+valflag+"'></div><i class='cr'>"+val+"</i>"
	}else if(valflag == '4'||valflag == '5'){
		return "<div class='wfl f"+valflag+"'></div><i class='cb'>"+val+"</i>"
	}else{
		return val;
	}
}

function DB_getHokaRate(hoka,standardjuka, obj) {
	if(hoka == '0'){
		return '0.00';
	}
	standardjuka = Number(standardjuka.replace(/[^0-9]/ig,''));
	hoka = Number(hoka.replace(/[^0-9]/ig,''));
	var res = (hoka - standardjuka) * 100.0 / standardjuka;
	return DB_getColBuho(res.toFixed(2));
}

function DB_getColBuho(val){
	if(val.indexOf("-")==0){
		return "<i class='cb'>"+val+"</i>";
	}else if(val === '0.00' || val === '0'||val === ''){
		return val;
	}else{
		return "<i class='cr'>+"+val+"</i>";
	}
}

function DB_getColBuhoWithUnit(val,unit){
	if(val.indexOf("-")==0){
		return "<i class='cb'>"+val+unit+"</i>";
	}else if(val === '0.00' || val === '0'||val === ''){
		return val+unit;
	}else{
		return "<i class='cr'>+"+val+unit+"</i>";
	}
}

function DB_concatIdName(val1,val2){
	return val1 +' ('+val2+')';
}















